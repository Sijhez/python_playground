# calculadora sencilla:

num1 = input("Ingresa un numero:")
num2 = input("Ingresa otro numero para agregar:")

num_1 = float(num1)
num_2 = float(num2)

print(num_1 , "+", num_2, "=", num_1+num_2 )
print(num_1 , "-", num_2, "=", num_1-num_2 )
print(num_1 , "*", num_2, "=", num_1*num_2 )
print(num_1 , "/", num_2, "=", num_1/num_2 )